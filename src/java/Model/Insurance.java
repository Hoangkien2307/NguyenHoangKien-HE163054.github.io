package Model;

/**
 *
 * @author kienb
 */
public class Insurance {
    private int id;
    private String name;
    private double percent;

    public Insurance() {
    }

    public Insurance(int id, String name, double percent) {
        this.id = id;
        this.name = name;
        this.percent = percent;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPercent() {
        return percent;
    }

    public void setPercent(double percent) {
        this.percent = percent;
    }
    
    
}
