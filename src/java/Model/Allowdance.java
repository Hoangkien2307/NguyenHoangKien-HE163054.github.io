package Model;

/**
 *
 * @author kienb
 */
public class Allowdance {

    private int id;
    private String name;
    private double money;

    public Allowdance() {
    }

    public Allowdance(int id, String name, double money) {
        this.id = id;
        this.name = name;
        this.money = money;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }

}
